package com.eros.soundtrack.enity;

import com.google.gson.annotations.SerializedName;

/**
 * Created by eroschen on 2017/8/31.
 */

public class Artist {
    @SerializedName("_id")
    private int id;

    @SerializedName("name")
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
